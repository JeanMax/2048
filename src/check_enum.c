/*
** check is the enum is valid
*/

#include "header.h"

void			check_enum(size_t j)
{
	size_t			i;

	i = 2;
	while (i < j && i < 3000000000)
		i *= 2;
	if (i != j)
		error(ENUM, NULL);
	if (i == 2147483648)
	{
		i = 1;
		while (i < 20000000)
		{
			i *= 2;
			i /= 2;
			i++;
		}
	}
}
